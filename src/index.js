const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};


function decode(expr) {
    let arr = [];
    for (let i = 0; i < expr.length / 10; i++){
    arr.push(expr.slice(i * 10, (i + 1) * 10));
    }
    let result = arr.map(elem => elem.replace(/10/g, '.').replace(/11/g, '-').replace(/00/g, '').replace('**********', ' '))
    .map(elem => elem == ' '? ' ':(MORSE_TABLE[elem])).join('');
    return result;
}

module.exports = {
    decode
}